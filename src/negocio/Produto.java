package negocio;

/**
 *
 * @author Ruan
 */
public class Produto {
    String descricao;
    float preco;
    boolean status;

    public Produto(String descricao, float preco, boolean status) {
        this.descricao = descricao;
        this.preco = preco;
        this.status = status;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
    
}
